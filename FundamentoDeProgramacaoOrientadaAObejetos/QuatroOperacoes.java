class QuatroOperacoes{
	public static void main(String args[]){
		int num1 = 5;
		int num2 = 2;
		int soma = num1+num2;
		int subtracao = num1-num2;
		int divisao = num1/num2;
		int multiplicacao= num1*num2;
		System.out.println("O valor obtido a partir da soma de " +num1+ " e " +num2+ " tera o resutado: " +soma);
		System.out.println("O resultado da subtração entre " +num1+ " e " +num2+ " e igual a " +subtracao);
		System.out.println("O resultado da divisao entre " +num1+ " e " +num2+ " sera " +divisao);
		System.out.println("O resultado da multiplicacao entre " +num1+ " e " +num2+ " sera " +multiplicacao);
	}
}