class Ex01{
	public static void main(String args[]){
		int num = -5;
		if (num==0){
			System.out.println("O valor " +num+  " eh zero");
		}
		else{
			if (num>0){
				System.out.println("O valor digitado eh positivo");
			}
			else{
				System.out.println("O valor digitado eh negativo");
			}
		}
	}
}